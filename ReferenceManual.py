def num_corresponding_to_maj_min_color(MAJOR_COLORS,MINOR_COLORS):
         num = 1
         print("-------------------------------------------")
         print(' | ',"NO.",' | ',"MAJOR",' | ',"MINOR",' | ')
         print("-------------------------------------------")
         for i in MAJOR_COLORS:
            for j in MINOR_COLORS:
               print(' | ',num,' | ',i,' | ',j,' | ')
               num=num+1
                      
                
